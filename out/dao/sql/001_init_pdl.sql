-- Limite max_choices (N parametre par l'admin)
CREATE OR REPLACE TRIGGER trg_choices_check_max
BEFORE INSERT ON choices
FOR EACH ROW
DECLARE
    v_count NUMBER;
    v_max NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_count
    FROM choices c
    WHERE c.campaign_id = :NEW.campaign_id
      AND c.student_id = :NEW.student_id;

    SELECT max_choices INTO v_max
    FROM campaigns
    WHERE id = :NEW.campaign_id;

    IF v_count >= v_max THEN
        raise_application_error(-20007, 'Maximum number of choices reached for this campaign');
    END IF;
END;
/

-- Eviter depassement de capacite pour ALLOCATED
CREATE OR REPLACE TRIGGER trg_registrations_capacity
BEFORE INSERT ON registrations
FOR EACH ROW
DECLARE
    v_allocated NUMBER;
    v_capacity NUMBER;
BEGIN
    IF :NEW.status = 'ALLOCATED' THEN
        SELECT COUNT(*) INTO v_allocated
        FROM registrations r
        WHERE r.campaign_id = :NEW.campaign_id
          AND r.session_id = :NEW.session_id
          AND r.status = 'ALLOCATED';

        SELECT capacity INTO v_capacity
        FROM sessions s
        WHERE s.id = :NEW.session_id;

        IF v_allocated >= v_capacity THEN
            raise_application_error(-20008, 'Session capacity exceeded');
        END IF;
    END IF;
END;
/

-- Verifier que l'utilisateur en choix est bien STUDENT
CREATE OR REPLACE TRIGGER trg_choices_student_only
BEFORE INSERT ON choices
FOR EACH ROW
DECLARE
    v_role users.role%TYPE;
BEGIN
    SELECT role INTO v_role FROM users WHERE id = :NEW.student_id;
    IF v_role <> 'STUDENT' THEN
        raise_application_error(-20009, 'Only STUDENT users can create choices');
    END IF;
END;
/

-- Cohérence: le campaign_id du choix doit correspondre à celui de la session
CREATE OR REPLACE TRIGGER trg_choices_campaign_match
BEFORE INSERT ON choices
FOR EACH ROW
DECLARE
    v_campaign_id sessions.campaign_id%TYPE;
BEGIN
    SELECT campaign_id INTO v_campaign_id FROM sessions WHERE id = :NEW.session_id;
    IF v_campaign_id <> :NEW.campaign_id THEN
        raise_application_error(-20010, 'Choice campaign_id must match session campaign_id');
    END IF;
END;
/

-- Cohérence: le campaign_id de l'inscription doit correspondre à celui de la session
CREATE OR REPLACE TRIGGER trg_registrations_campaign_match
BEFORE INSERT ON registrations
FOR EACH ROW
DECLARE
    v_campaign_id sessions.campaign_id%TYPE;
BEGIN
    SELECT campaign_id INTO v_campaign_id FROM sessions WHERE id = :NEW.session_id;
    IF v_campaign_id <> :NEW.campaign_id THEN
        raise_application_error(-20011, 'Registration campaign_id must match session campaign_id');
    END IF;
END;
/
