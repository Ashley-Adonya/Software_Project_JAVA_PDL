-- ===========================================================
-- Oracle schema init for PDL
-- This script drops existing tables first, then recreates schema.
-- ===========================================================

-- 0) Clean existing schema objects (in dependency order)
BEGIN
    FOR t IN (
        SELECT table_name
        FROM user_tables
        WHERE table_name IN (
            'REGISTRATIONS',
            'CHOICES',
            'SESSIONS',
            'CAMPAIGNS',
            'DOMINANTES',
            'USERS'
        )
    ) LOOP
        EXECUTE IMMEDIATE 'DROP TABLE ' || t.table_name || ' CASCADE CONSTRAINTS PURGE';
    END LOOP;
EXCEPTION
    WHEN OTHERS THEN
        NULL;
END;
/

-- 1) Utilisateurs (admin + etudiants)
CREATE TABLE users (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    login VARCHAR2(100) NOT NULL UNIQUE,
    password VARCHAR2(255) NOT NULL,
    full_name VARCHAR2(200) NOT NULL,
    role VARCHAR2(20) NOT NULL CHECK (role IN ('ADMIN', 'STUDENT')),
    promo VARCHAR2(100),
    is_active NUMBER(1) DEFAULT 1 NOT NULL CHECK (is_active IN (0, 1)),
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
);

-- 2) Dominantes
CREATE TABLE dominantes (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code VARCHAR2(50) NOT NULL UNIQUE,
    name VARCHAR2(200) NOT NULL UNIQUE,
    responsible_name VARCHAR2(200) NOT NULL,
    description CLOB,
    color VARCHAR2(20) DEFAULT '#3b82f6',
    is_active NUMBER(1) DEFAULT 1 NOT NULL CHECK (is_active IN (0, 1))
);

-- 3) Campagnes d'inscription
CREATE TABLE campaigns (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(200) NOT NULL UNIQUE,
    promo VARCHAR2(100) NOT NULL,
    registration_day DATE NOT NULL,
    start_date DATE,
    end_date DATE,
    max_choices NUMBER NOT NULL CHECK (max_choices > 0),
    status VARCHAR2(20) DEFAULT 'PREPARATION' NOT NULL
        CHECK (status IN ('PREPARATION', 'OPEN', 'CLOSED', 'PROCESSING', 'VALIDATED', 'ARCHIVED')),
    opened_at TIMESTAMP,
    closed_at TIMESTAMP,
    processed_at TIMESTAMP,
    validated_at TIMESTAMP,
    archived_at TIMESTAMP,
    created_by NUMBER NOT NULL,
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT fk_campaigns_created_by
        FOREIGN KEY (created_by) REFERENCES users(id)
);

-- 4) Sessions : 08:30-12:30 et 13:30-17:30
-- Representation en minutes depuis minuit.
CREATE TABLE sessions (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    campaign_id NUMBER NOT NULL,
    dominante_id NUMBER NOT NULL,
    title VARCHAR2(200) NOT NULL,
    room VARCHAR2(100),
    session_date DATE NOT NULL,
    start_minute NUMBER NOT NULL,
    end_minute NUMBER NOT NULL,
    capacity NUMBER NOT NULL CHECK (capacity > 0),
    created_by NUMBER NOT NULL,
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_at TIMESTAMP,
    CONSTRAINT fk_sessions_campaign
        FOREIGN KEY (campaign_id) REFERENCES campaigns(id) ON DELETE CASCADE,
    CONSTRAINT fk_sessions_dominante
        FOREIGN KEY (dominante_id) REFERENCES dominantes(id),
    CONSTRAINT fk_sessions_created_by
        FOREIGN KEY (created_by) REFERENCES users(id),
    CONSTRAINT ck_sessions_duration
        CHECK (end_minute > start_minute),
    CONSTRAINT ck_sessions_start_window
        CHECK (
            (start_minute BETWEEN 510 AND 720) OR
            (start_minute BETWEEN 810 AND 1020)
        ),
    CONSTRAINT ck_sessions_end_window
        CHECK (
            (end_minute BETWEEN 511 AND 750) OR
            (end_minute BETWEEN 811 AND 1050)
        )
);

-- Eviter duplicat de meme session sur meme dominante/campagne/date/creneau
CREATE UNIQUE INDEX ux_session_slot
    ON sessions (campaign_id, dominante_id, session_date, start_minute);

-- 5) Choix etudiants (liste ordonnee)
CREATE TABLE choices (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    campaign_id NUMBER NOT NULL,
    student_id NUMBER NOT NULL,
    session_id NUMBER NOT NULL,
    rank_order NUMBER NOT NULL CHECK (rank_order > 0),
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_at TIMESTAMP,
    CONSTRAINT fk_choices_campaign
        FOREIGN KEY (campaign_id) REFERENCES campaigns(id) ON DELETE CASCADE,
    CONSTRAINT fk_choices_student
        FOREIGN KEY (student_id) REFERENCES users(id) ON DELETE CASCADE,
    CONSTRAINT fk_choices_session
        FOREIGN KEY (session_id) REFERENCES sessions(id) ON DELETE CASCADE
);

-- Un etudiant ne peut pas mettre deux fois le meme rang
CREATE UNIQUE INDEX ux_choice_student_rank
    ON choices (campaign_id, student_id, rank_order);

-- Un etudiant ne peut pas choisir 2 fois la meme session
CREATE UNIQUE INDEX ux_choice_student_session
    ON choices (campaign_id, student_id, session_id);

-- 6) Inscriptions (resultat du traitement)
CREATE TABLE registrations (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    campaign_id NUMBER NOT NULL,
    student_id NUMBER NOT NULL,
    session_id NUMBER NOT NULL,
    source_choice_rank NUMBER,
    status VARCHAR2(20) DEFAULT 'ALLOCATED' NOT NULL
        CHECK (status IN ('ALLOCATED', 'WAITLIST', 'REJECTED', 'CANCELLED')),
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_at TIMESTAMP,
    CONSTRAINT fk_registrations_campaign
        FOREIGN KEY (campaign_id) REFERENCES campaigns(id) ON DELETE CASCADE,
    CONSTRAINT fk_registrations_student
        FOREIGN KEY (student_id) REFERENCES users(id) ON DELETE CASCADE,
    CONSTRAINT fk_registrations_session
        FOREIGN KEY (session_id) REFERENCES sessions(id) ON DELETE CASCADE,
    CONSTRAINT uq_registrations_campaign_student_session
        UNIQUE (campaign_id, student_id, session_id)
);

-- ===== Triggers metier =====

-- Sessions modifiables seulement en PREPARATION
CREATE OR REPLACE TRIGGER trg_sessions_insert_only_preparation
BEFORE INSERT ON sessions
FOR EACH ROW
DECLARE
    v_status campaigns.status%TYPE;
BEGIN
    SELECT status INTO v_status FROM campaigns WHERE id = :NEW.campaign_id;
    IF v_status <> 'PREPARATION' THEN
        raise_application_error(-20001, 'Sessions can only be created in PREPARATION status');
    END IF;
END;
/

